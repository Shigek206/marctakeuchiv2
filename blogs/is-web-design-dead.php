<h3>Web Design is thriving more than ever</h3>
<p>Those that say web design is dead couldn't be more wrong. Web design is and always will be a critical part of a web site from the large to small, corporate to personal, the first thing a user sees when going to a web site is its design or what is known as 'look and feel' or 'branding'. It is one aspect that makes it unique from your competitors and one of a kind.</p>

<p>So how important is a web site design? With millions of sites on the internet, imagine if all of them used the same layout, no graphics, black text on a white background and one font type and size. If you visited a hundred sites would you be able to identify one from another by their look? Design also has the power to create curiosity amongst its viewers causing one to explore to find out more about it while enhancing the user experience. One of the most important things to remember is to take your time and carefully brand your site. Here are some foundational questions to help develop an effective branding strategy:</p>

<ul>
	<li>What is your business and industry?</li> 
    <li>What is the purpose/objective of your web site?</li>
    <li>Who is your intended users and audience?</li>
    <li>What message(s) are you trying to convey?</li>
</ul>

<p>Determining the answers to these questions will help guide your team to branding success.</p>

