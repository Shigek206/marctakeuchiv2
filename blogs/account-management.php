<h3>Recipe for account management</h3>

<p>So the million dollar question I get asked is what are the ingredients to be a successful account manager? Whatever field you're in there are key fundamentals that will help set you up and prepare you for success. Granted not every project will be smooth from start to finish nor will every client be easy going however going in with a correct mindset will prepare you to work through any situation. If you lack any of these, you won't necessarily fail, however it will makes more difficult, trust me. 

<h2>Have a open mind</h2>
<p>We all experience different situations in life. New relationship, new job, living in a new city or even playing a new golf course, it could be anythig. The one thing I tell people is whatever the situation, always go in with an open mind. My saying is "hope for the best, expect anything", not "expect the worst". If you go in to a sitation expecting the worst you're at a disadvantage as you're going in with added pressure and a negatve attitude. I can't predict the outcome of a project  



</p>



